            <p style='text-align: justify;'>Transfer pricing refers to the pricing of contributions (assets, tangible and intangible, services, and funds) transferred within an organization. For example, goods from the production division may be sold to the marketing division, or goods from a parent company may be sold to a foreign subsidiary. Since the prices are set within an organisation (i.e., controlled), the typical market mechanisms that establish prices for such transactions between third parties may not apply. The choice of the transfer price will affect the allocation of the total profit among the parts of the company. This is a major concern for fiscal authorities who worry that multi-national entities may set transfer prices on cross-border transactions to reduce taxable profits in their jurisdiction. This has led to the rise of transfer pricing regulations and enforcement, making transfer pricing a major tax compliance issue for multi-national companies.</p>
