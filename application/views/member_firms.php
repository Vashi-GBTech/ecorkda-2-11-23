                <h4>Purpose</h4>
                <div class="">
                    <p style='text-align: justify;'>This paper provides guidance to Rkda member
                        firms on the manner in which complaints are dealt with in cases where a member firm is not
                        satisfied with the quality of the services received, either directly or through a client, from
                        another member firm.
                    </p>
                    <h4 class="mb-0" >Overview of Complaints Process</h4>
                    <h6> <strong> Stage I – Addressing your complaint to the other member firm </strong></h6>
                    <p style='text-align: justify;' class="mt-3">You should first address your
                        complaint to the Rkda adviser/firm who is responsible for the provision of services to you
                        and/or your client. Experience has shown that many issues are resolved through an initial phone
                        call. We thus recommend an oral communication, if possible, as a first step. If the matter is
                        not resolved, you should then formally communicate in writing with the member firm outlining a
                        full description of the problem. If necessary, please refer to the member firm’s website or the
                        members’ directory to identify the partners in charge/responsible partner for quality issues. If
                        you need assistance please contact the back office of Rkda International via
                        international@Rkda.com.</p>

                    <p>It is important to detail the circumstances of the problem and its nature and whether this
                        relates to a lack of response, a pricing issue quality of service etc.</p>

                    <p>
                        The member firm receiving a complaint should acknowledge receipt of the complaint within 48
                        hours. Thereafter, a written response to the complaint should be sent within a maximum of 30
                        days or, if not possible, an estimate of when a response can be expected.
                    </p>

                    <p>In your response to the member firm it is expected that you will either (i) provide information
                        about the steps you will take to resolve the issue or (ii) provide an explanation as to why you
                        believe the complaint is not valid.</p>

                    <p>The trust we have and should expect between all our member firms and the expectation of quality
                        and professionalism in serving mutual clients is essential for the network to operate
                        efficiently.</p>

                    <h6 class="my-4" ><strong>Stage II – Addressing your complaint to the Rkda International back office</strong> </h6>

                    <p>If you are not satisfied with the adviser’s/member firm’s response in Stage I or you have not
                        received any answer you can make a formal complaint by sending an email to
                        international@Rkda.com .</p>

                    <p>Please provide the following information:</p>

                    <ul class='internship-pt-ul p-0'>
                        <li>Your name and contact details (if not available in your mail signature).</li>
                        <li>Name of the Rkda member firm.</li>
                        <li>The nature of the engagement with the Rkda member firm.</li>
                        <li>Responsible partner and advisor within the Rkda member firm.</li>
                        <li>Details of the complaint (including when the performance giving rise to the complaint
                            occurred).</li>
                        <li>Copies of any documentation in support of your complaint.</li>
                        <li>With reference to Stage I: Please indicate when and how you notified the Rkda member firm
                            about your dissatisfaction, the level of response and the reasons why such a response is
                            unsatisfactory in your opinion.</li>
                        <li>If possible, please indicate if your complaint is of technical, organisational or ethical
                            nature.</li>
                    </ul>
                     <h6 class="my-4" >
                        <strong>  Stage III – Acknowledging your complaint and internal investigation</strong>
                    </h6>

                     <p>We will acknowledge the receipt of your complaint within two working days. Technical complaints will be presented to the Quality Development Board which can process the complaint directly, assign it to a sub-committee or refer to the Management Board if the nature, severity or consequences for the network warrants it. Organisational and ethical complaints will be presented to the Management Board. Both boards aim to resolve your complaint as soon as possible, generally expected to be within 30 days. We will inform you in case the review is likely to take longer and explain why our response is delayed. It will be indicated when we expect to complete the review and provide the board’s response to your complaint.</p>

                     <p>You can contact the individual assigned to your case at any time to inquire about the status of the review.</p>
                     <p>Complaints should only be addressed to the above e-mail address and will be passed only to the Quality Development or Management Boards.</p>

                     <p>Should your complaint not be dealt with within the time-frame outlined above you should raise this matter with the Supervisory Board.</p>

                     <h6 class="my-4" > <strong>Stage IV – Written response to your complaint</strong>  </h6>

                     <p>When we have completed our review of your complaint, you will receive a response. Our overall aim is to improve the quality of the work as well as the communication between members across the network. Our response will be framed in this context and will be focused not so much in judging the situation and/or apportioning blame. We will be seeking to propose ways in which work can be improved such that we can increase the global reliability of the network. The response will be confidential and destined only for the member firms involved, without prejudice to any action the Management Board might take considering the nature of any infraction and the overall history of complaints.</p>

                     <h4 class="mb-0">Miscellaneous:</h4>
                     <p>In case a member firm receives three complaints which are considered as valid, the Management Board will refer the matter for discussion with the Supervisory Board. These discussions will consider any proposed solutions as well as any consequences for the Rkda member (based on the severity of the complaints). In case of very serious complaints the engagement of the Supervisory Board may be brought forward.</p>
                     <h4 class="mb-0">Mediation:</h4>
                     <p>It is also noted that member firms may seek mediation as per the <a href="#"> Rkda mediation rules.</a></p>
